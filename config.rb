# Methods defined in the helpers block are available in templates
helpers do
  def navbar_item(title, url, page)
    canonical_path = "/#{page.path}".gsub(/index\.html\z/, '')
    
    li_attrs = if canonical_path == url
      { class: "active" }
    else
      {}
    end
    
    content_tag(:li, li_attrs) do
      link_to title, url
    end
  end
end

